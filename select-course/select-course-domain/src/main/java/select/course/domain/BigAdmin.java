package select.course.domain;

public class BigAdmin {
    private Integer id;
    private String badminNum;
    private String passWord;
    private String name;
    private String duty;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getBadminNum() {
        return badminNum;
    }

    public void setBadminNum(String badminNum) {
        this.badminNum = badminNum;
    }

    public String getPassWord() {
        return passWord;
    }

    public void setPassWord(String passWord) {
        this.passWord = passWord;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDuty() {
        return duty;
    }

    public void setDuty(String duty) {
        this.duty = duty;
    }
}
